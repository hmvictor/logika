    public Section open(List<Section> sections) {
        DefaultListModel<Section> defaultListModel = new DefaultListModel<>();
        defaultListModel.addAll(sections);
        sectionList.setModel(defaultListModel);
        sectionList.setCellRenderer(new DefaultListCellRenderer(){
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                Component component = super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                Section section = (Section)value;
                ((JLabel)component).setText(section.getArgument() == null ? "(Empty)": section.getArgument().replaceAll("\n", " / "));
                return component;
            }
            
        });
        setVisible(true);
        return sectionList.getSelectedValue();
    }

